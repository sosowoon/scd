package bean;

public class OpclDTO {
	String commercialDistrictCode;
	String commercialDistrictName;
	int year;
	int quarter;
	String sellCategoryName;
	int op;
	int cl;
	double latitude;
	double longitude;
	public String getCommercialDistrictCode() {
		return commercialDistrictCode;
	}
	public void setCommercialDistrictCode(String commercialDistrictCode) {
		this.commercialDistrictCode = commercialDistrictCode;
	}
	public String getCommercialDistrictName() {
		return commercialDistrictName;
	}
	public void setCommercialDistrictName(String commercialDistrictName) {
		this.commercialDistrictName = commercialDistrictName;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getQuarter() {
		return quarter;
	}
	public void setQuarter(int quarter) {
		this.quarter = quarter;
	}
	public String getSellCategoryName() {
		return sellCategoryName;
	}
	public void setSellCategoryName(String sellCategoryName) {
		this.sellCategoryName = sellCategoryName;
	}
	public int getOp() {
		return op;
	}
	public void setOp(int op) {
		this.op = op;
	}
	public int getCl() {
		return cl;
	}
	public void setCl(int cl) {
		this.cl = cl;
	}
	public double getLatitude() {
		return latitude;
	}
	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}
	public double getLongitude() {
		return longitude;
	}
	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}
	
}
